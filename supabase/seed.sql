-- Sesión ficticia de ejemplo en OOAD Prueba Norte, usando un tema real del catálogo
with sesion_nueva as (
  insert into sesiones (ooad_id, numero_sesion, tipo, fecha, sede, quorum_certificado, estatus)
  select id, 1, 'ordinaria', date '2026-02-10', 'Sala de juntas ficticia', true, 'celebrada'
  from ooads where nombre = 'OOAD Prueba Norte'
  returning id
),
punto_nuevo as (
  insert into puntos_sesion (sesion_id, tema_catalogo_id, texto_literal, orden)
  select
    sesion_nueva.id,
    (select id from temas_catalogo where tema = 'Ausentismo Programado y No Programado' and subtema = 'Vigilar que el Comité sesione mensualmente.'),
    'Se revisa la periodicidad de las sesiones del Comité en la unidad ficticia de prueba.',
    1
  from sesion_nueva
  returning id, sesion_id
)
insert into acuerdos (sesion_id, punto_sesion_id, texto_literal, fecha_compromiso, responsable, estatus)
select
  punto_nuevo.sesion_id,
  punto_nuevo.id,
  'Se acuerda calendarizar las sesiones mensuales del Comité en la unidad ficticia de prueba.',
  null,
  null,
  'sin_seguimiento'
from punto_nuevo;
